def divide(a: float, b: float) -> float:
    """Add two numbers together.

    Does simple division.

    Args:
      a: float The first number in the numerator.
      b: float The second number in the denominator.

    Returns:
      float The division of ``a`` divided ``b``.

    Examples:
        >>> divide(4, 2)
        2
        >>> divide(5, 1)
        5
    """
    return a / b

